class TestIp4RxRfc791OptionPreservedOnReassembly(Ip4TestCase):
    """
    The RFC 815 §6 / RFC 791 §3.1 option-preservation-on-
    reassembly tests.

    On reassembly the IPv4 RX handler MUST preserve the first
    fragment's options bytes (which carry the canonical option
    set for the reassembled datagram per RFC 815 §6). Before
    the fix the handler stripped all options by forcing IHL=5;
    after the fix the original IHL + options are carried
    through to the upper layer.
    """

    def _build_fragment(
        self,
        *,
        offset: int,
        flag_mf: bool,
        payload_chunk: bytes,
        options: Ip4Options,
        frag_id: int,
    ) -> bytes:
        """
        Build a wire-format Ethernet+IPv4-frag frame as a single
        bytes object. Uses Ip4FragAssembler so we don't have to
        hand-roll checksums.
        """

        ip4_frag = Ip4FragAssembler(
            ip4_frag__src=HOST_A__IP4_ADDRESS,
            ip4_frag__dst=STACK__IP4_HOST.address,
            ip4_frag__id=frag_id,
            ip4_frag__offset=offset,
            ip4_frag__flag_mf=flag_mf,
            ip4_frag__proto=IpProto.from_int(99),
            ip4_frag__options=options,
            ip4_frag__payload=payload_chunk,
        )
        eth = EthernetAssembler(
            ethernet__src=MacAddress("02:00:00:00:00:91"),
            ethernet__dst=MacAddress("02:00:00:00:00:07"),
            ethernet__payload=ip4_frag,
        )
        return bytes(eth)

    def test__ip4__rx__reassembly_preserves_first_fragment_options(self) -> None:
        """
        Ensure two received IPv4 fragments where the first
        carries [Router Alert + RR + NOP] and the second
        carries the copy-flag=1 subset reassemble into a single
        datagram whose IPv4 header preserves the FULL first-
        fragment option set — the first fragment is canonical
        for the reassembled datagram's options.

        Reference: RFC 815 §6 (first fragment options canonical).
        Reference: RFC 791 §3.1 (options copy-flag drives the
        per-fragment subset; first fragment carries full set).
        """

        # Router Alert (copy=1, 4 bytes) + RR (copy=0, 7 bytes) +
        # NOP padding = 12 bytes on first fragment. Avoid
        # LSRR/SSRR which trigger PyTCP's source-route drop gate
        # (IP4__ACCEPT_SOURCE_ROUTE=False) so the test exercises
        # option-preservation directly without policy gating.
        first_options = Ip4Options(
            Ip4OptionRouterAlert(),
            Ip4OptionRr(pointer=4, route=[Ip4Address("0.0.0.0")]),
            Ip4OptionNop(),
        )
        # Subsequent fragment options: copy_flag=1 (Router Alert,
        # already 4 bytes — no padding needed). Mirror what the
        # TX fragmenter would actually emit.
        non_first_options = Ip4Options(Ip4OptionRouterAlert())

        frame_first = self._build_fragment(
            offset=0,
            flag_mf=True,
            payload_chunk=b"A" * 16,
            options=first_options,
            frag_id=0x1234,
        )
        frame_second = self._build_fragment(
            offset=16,
            flag_mf=False,
            payload_chunk=b"B" * 16,
            options=non_first_options,
            frag_id=0x1234,
        )

        self._packet_handler._phrx_ethernet(PacketRx(frame_first))
        self._packet_handler._phrx_ethernet(PacketRx(frame_second))

        # Unknown proto 99 → ICMPv4 Protocol Unreachable
        # response. The response's IPv4 payload is an ICMP
        # message whose 'data' field carries the reassembled
        # IPv4 packet bytes — including the preserved options.
        self.assertEqual(
            len(self._frames_tx),
            1,
            msg="Unknown-proto reassembled datagram must elicit one ICMP Protocol Unreachable response.",
        )

        # Extract the inner IPv4 packet from the ICMPv4 message
        # data field of the response. Layout of the response:
        #   Ethernet (14) | IPv4 (20) | ICMP msg (8 + quoted)
        # The quoted bytes start at offset 14+20+8 = 42.
        response = self._frames_tx[0]
        quoted_ip4_bytes = response[14 + 20 + 8 :]

        # Parse the quoted IPv4 bytes — this is the reassembled
        # datagram that triggered the Protocol Unreachable.
        reassembled = Ip4Parser(PacketRx(quoted_ip4_bytes))

        # The reassembled packet must carry the full first-
        # fragment options (Router Alert + RR + NOP).
        self.assertEqual(
            [int(o.type) for o in reassembled.options],
            [148, 7, 1],
            msg="Reassembled datagram must preserve the first fragment's full options.",
        )
        # IHL must reflect the preserved header length (20 base
        # + 12 options = 32 bytes → IHL=8).
        self.assertEqual(
            reassembled.hlen,
            32,
            msg="Reassembled IHL must reflect the preserved options length.",
        )
        # Total Length must include both header and payload.
        self.assertEqual(
            reassembled.plen,
            32 + 32,
            msg="Reassembled Total Length must equal header bytes (incl. options) + payload bytes.",
        )

    def test__ip4__rx__reassembly_with_no_options_uses_minimum_ihl(self) -> None:
        """
        Ensure a no-options two-fragment reassembly produces a
        reassembled datagram with IHL=5 (no options) — regression
        net for the no-options case that the on-touch fix must
        not break.

        Reference: PyTCP test infrastructure (regression net
        for the RFC 815 §6 fix).
        """

        empty = Ip4Options()
        frame_first = self._build_fragment(
            offset=0,
            flag_mf=True,
            payload_chunk=b"A" * 16,
            options=empty,
            frag_id=0x5678,
        )
        frame_second = self._build_fragment(
            offset=16,
            flag_mf=False,
            payload_chunk=b"B" * 16,
            options=empty,
            frag_id=0x5678,
        )

        self._packet_handler._phrx_ethernet(PacketRx(frame_first))
        self._packet_handler._phrx_ethernet(PacketRx(frame_second))

        response = self._frames_tx[0]
        quoted_ip4_bytes = response[14 + 20 + 8 :]
        reassembled = Ip4Parser(PacketRx(quoted_ip4_bytes))

        self.assertEqual(
            reassembled.hlen,
            20,
            msg="No-options reassembly must produce IHL=5 (20-byte header).",
        )
        self.assertEqual(
            list(reassembled.options),
            [],
            msg="No-options reassembly must produce an empty option set.",
        )


class TestIp4RxRfc3168EcnAggregationOnReassembly(Ip4TestCase):
    """
    The RFC 3168 §5.3 ECN-aggregation-on-reassembly tests.

    On reassembly the IPv4 RX handler MUST aggregate the per-
    fragment ECN codepoints into a single value in the
    reassembled datagram's TOS-byte low-2 bits. The aggregation
    follows Linux 'ip_frag_ecn_table[]' semantics: any-same
    yields that codepoint, CE+ECT yields CE, ECT(0)+ECT(1)
    yields ECT(0), and any mix of Not-ECT with any other
    codepoint MUST drop the reassembled datagram.
    """

    def _build_fragment(
        self,
        *,
        offset: int,
        flag_mf: bool,
        payload_chunk: bytes,
        ecn: int,
        frag_id: int,
    ) -> bytes:
        """
        Build a wire-format Ethernet+IPv4-frag frame carrying the
        named ECN codepoint in the IPv4 TOS-byte's low-2 bits.
        """

        ip4_frag = Ip4FragAssembler(
            ip4_frag__src=HOST_A__IP4_ADDRESS,
            ip4_frag__dst=STACK__IP4_HOST.address,
            ip4_frag__id=frag_id,
            ip4_frag__offset=offset,
            ip4_frag__flag_mf=flag_mf,
            ip4_frag__proto=IpProto.from_int(99),
            ip4_frag__ecn=ecn,
            ip4_frag__payload=payload_chunk,
        )
        eth = EthernetAssembler(
            ethernet__src=MacAddress("02:00:00:00:00:91"),
            ethernet__dst=MacAddress("02:00:00:00:00:07"),
            ethernet__payload=ip4_frag,
        )
        return bytes(eth)

    def _drive_two_fragment_flow(
        self,
        *,
        first_ecn: int,
        second_ecn: int,
        frag_id: int,
    ) -> None:
        """
        Drive a two-fragment flow into the RX path: first
        fragment carries 'first_ecn' at offset 0 with MF=1;
        second fragment carries 'second_ecn' at offset 16 with
        MF=0 (last). Both halves use the canonical proto-99
        unknown-proto trigger so a successful reassembly elicits
        a single ICMPv4 Protocol Unreachable response carrying
        the reassembled datagram in its quoted bytes.
        """

        frame_first = self._build_fragment(
            offset=0,
            flag_mf=True,
            payload_chunk=b"A" * 16,
            ecn=first_ecn,
            frag_id=frag_id,
        )
        frame_second = self._build_fragment(
            offset=16,
            flag_mf=False,
            payload_chunk=b"B" * 16,
            ecn=second_ecn,
            frag_id=frag_id,
        )
        self._packet_handler._phrx_ethernet(PacketRx(frame_first))
        self._packet_handler._phrx_ethernet(PacketRx(frame_second))

    def test__ip4__rx__reassembly_same_ecn_propagates_codepoint(self) -> None:
        """
        Ensure two fragments that both carry ECT(0) reassemble
        into a datagram whose ECN field is ECT(0) — the all-
        same-codepoint fast path through the aggregation table.

        Reference: RFC 3168 §5.3 (all-same-codepoint preserves the codepoint).
        """

        self._drive_two_fragment_flow(first_ecn=2, second_ecn=2, frag_id=0xAA01)

        self.assertEqual(
            len(self._frames_tx),
            1,
            msg="Consistent-ECN reassembly must elicit one ICMP Protocol Unreachable response.",
        )

        response = self._frames_tx[0]
        quoted_ip4_bytes = response[14 + 20 + 8 :]
        reassembled = Ip4Parser(PacketRx(quoted_ip4_bytes))

        self.assertEqual(
            reassembled.ecn,
            2,
            msg="ECT(0)+ECT(0) reassembly must propagate ECT(0) on the wire.",
        )

    def test__ip4__rx__reassembly_ce_plus_ect_aggregates_to_ce(self) -> None:
        """
        Ensure two fragments — one CE, one ECT(0) — reassemble
        into a datagram whose ECN field is CE; the §5.3
        aggregation marks the reassembled datagram as
        congestion-experienced because at least one fragment
        carried CE.

        Reference: RFC 3168 §5.3 (CE+ECT(0) -> CE).
        """

        self._drive_two_fragment_flow(first_ecn=2, second_ecn=3, frag_id=0xAA02)

        self.assertEqual(
            len(self._frames_tx),
            1,
            msg="CE+ECT(0) reassembly must elicit one ICMP Protocol Unreachable response.",
        )

        response = self._frames_tx[0]
        quoted_ip4_bytes = response[14 + 20 + 8 :]
        reassembled = Ip4Parser(PacketRx(quoted_ip4_bytes))

        self.assertEqual(
            reassembled.ecn,
            3,
            msg="CE+ECT(0) reassembly must propagate CE on the wire.",
        )

    def test__ip4__rx__reassembly_ect0_plus_ect1_aggregates_to_ect0(self) -> None:
        """
        Ensure two fragments — one ECT(0), one ECT(1) —
        reassemble into a datagram whose ECN field is ECT(0);
        the §5.3 aggregation collapses a no-CE mixed-ECT set to
        ECT(0) (the Linux-canonical choice).

        Reference: RFC 3168 §5.3 (ECT(0)+ECT(1) -> ECT(0)).
        """

        self._drive_two_fragment_flow(first_ecn=2, second_ecn=1, frag_id=0xAA03)

        self.assertEqual(
            len(self._frames_tx),
            1,
            msg="ECT(0)+ECT(1) reassembly must elicit one ICMP Protocol Unreachable response.",
        )

